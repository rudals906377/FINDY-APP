def normalize_beauty_category(category):
    """Return a canonical FINDY category label."""
    return CATEGORY_ALIAS_MAP.get(str(category).strip(), str(category).strip())


def keywords_for_category(category):
    """Return keyword groups for a canonical or alias category."""
    normalized = normalize_beauty_category(category)
    return BEAUTY_MOOD_KEYWORDS.get(normalized, {})


def all_keywords(category=None):
    """Flatten keywords for one category or all categories."""
    categories = [normalize_beauty_category(category)] if category else BEAUTY_CATEGORIES
    result = []
    for category_name in categories:
        groups = BEAUTY_MOOD_KEYWORDS.get(category_name, {})
        for value in groups.values():
            if isinstance(value, list):
                result.extend(value)
    return sorted(set(result))
